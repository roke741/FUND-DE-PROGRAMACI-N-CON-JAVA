    int posicionActual = 10;
    int incremento = 10;
    public void desplazamiento(){
        Timer mTimer = new Timer(5, (e) -> {
            posicionActual += incremento;
            if (posicionActual <= 460 ) {
                lblBgImg.setLocation(posicionActual, 10);
                //System.out.println(lblBgImg.getLocation());
            }
        });
        mTimer.setRepeats(true);
        mTimer.restart();
        posicionActual=10;
    }
