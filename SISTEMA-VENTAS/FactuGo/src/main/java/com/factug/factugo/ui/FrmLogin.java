    int posicionActual1 = 460;
    public void desplazamientoNegativo(){
        Timer mTimer = new Timer(5, (e) -> {
            posicionActual1 -= incremento;
            if (posicionActual1 >= 10 ) {
                lblBgImg.setLocation(posicionActual1, 10);
                //System.out.println(lblBgImg.getLocation());
            }
        });
        mTimer.setRepeats(true);
        mTimer.restart();
        posicionActual1 = 460;
    }
