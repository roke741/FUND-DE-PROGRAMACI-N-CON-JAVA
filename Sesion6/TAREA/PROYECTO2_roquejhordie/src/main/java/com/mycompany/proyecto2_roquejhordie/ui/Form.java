    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double sueldoB = Double.parseDouble(txtSueldo.getText());
        double bnFamiliar = sueldoB * 0.09;
        double bnProduccion = sueldoB *  0.075;
        double dsctEPS = sueldoB * 0.15;
        double bnEspecial = 0;
        double dsctFaltas = 0;
        int faltas = Integer.parseInt(spnFaltas.getValue().toString());
        if( faltas > 0 ){
            dsctFaltas = (sueldoB * 0.042) * faltas;
        }else{
            bnEspecial = sueldoB * 0.05;
        }
        double sueldoN = (sueldoB + bnFamiliar + bnProduccion + bnEspecial)- dsctEPS - dsctFaltas;

        txtSalida.setText("");
        txtSalida.append("SUELDO NETO: " + sueldoN  +"\n");
        txtSalida.append("Detalles: \n" );
        txtSalida.append("  Bonificaciones: \n");
        txtSalida.append("      Bn. Familiar: " + bnFamiliar + "\n" );
        txtSalida.append("      Bn. Produccion: " + bnProduccion + "\n" );
        txtSalida.append("      Bn. Especial: " + bnEspecial + "\n" );
        txtSalida.append("  Descuentos: \n");
        txtSalida.append("      Dsct. EPS: " + dsctEPS + "\n" );
        txtSalida.append("      Dsct. Faltas: " + dsctFaltas + "\n" );
    }//GEN-LAST:event_btnCalcularActionPerformed

